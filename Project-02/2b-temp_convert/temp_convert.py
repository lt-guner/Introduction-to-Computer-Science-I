# Description: This converts Celsius to Fahrenheit

# ask user to enter a celsius and save it to celsius
print("Please enter a Celsius temperature.")
celsius = float(input())

# convert to fahrenheit and print
fahrenheit = celsius * (9 / 5) + 32
print("The equivalent Fahrenheit temperature is:")
print(fahrenheit)